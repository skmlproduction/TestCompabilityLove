def _insert_locale_chip(svg: str) -> str:
    if "localeChip" in svg:
        return svg
    svg = svg.replace("    ]]></style>", LOCALE_CHIP_STYLE + "    ]]></style>", 1)
    # After status bar block (before first nav/back or title)
    anchor = '  <rect x="900" y="49" width="40" height="10" rx="5" fill="#1C1B1F" opacity="0.72"/>\n'
    if anchor in svg:
        return svg.replace(anchor, anchor + LOCALE_CHIP_MARKUP, 1)
    return svg.replace("<rect width=\"1080\"", LOCALE_CHIP_MARKUP + "\n  <rect width=\"1080\"", 1)


def translate_svg(src_name: str, dst_name: str) -> None:
    text = (DESIGN / src_name).read_text(encoding="utf-8")
    for old, new in sorted(TEXT_REPLACEMENTS, key=lambda p: len(p[0]), reverse=True):
        text = text.replace(old, new)
    for old, new in SECTION_REPLACEMENTS:
        text = text.replace(old, new)
    text = _insert_locale_chip(text)
    out = DESIGN / dst_name
    out.write_text(text, encoding="utf-8")
    print(f"  wrote {out.name}")


def main() -> int:
    print("generate_protocol_en_svgs:")
    for src, dst in JOBS:
        if not (DESIGN / src).is_file():
            print(f"  missing source: {src}")
            return 1
        translate_svg(src, dst)
    print(f"  done ({len(JOBS)} files)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
